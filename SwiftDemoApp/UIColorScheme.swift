import UIKit

extension UIColor {
    static var darkOrange: UIColor {
        return UIColor(red: 0.9, green: 0.3, blue: 0, alpha: 1)
    }
    static var darkGreen: UIColor {
        return UIColor(red: 0.1, green: 0.7, blue: 0.1, alpha: 1)
    }
}
